/**
 * =================================================================================================
 *  GAME WIN
 * =================================================================================================
 */
function gameWin(gameInterval, score) {
  clearInterval(gameInterval);
  alert("You win! Your score is: " + score);
}

/**
 * =================================================================================================
 *  GAME OVER
 * =================================================================================================
 */
function gameOver(gameInterval, restartGame) {
  clearInterval(gameInterval);
  alert("Game over!");

  restartGame();
}

/**
 * =================================================================================================
 *  RESTART GAME
 * =================================================================================================
 */

function restartGame() {
  location.reload();
}
